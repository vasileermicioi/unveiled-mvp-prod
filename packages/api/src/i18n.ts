export type UiLanguage = "DE" | "EN";

export const supportedLanguages = ["DE", "EN"] as const;
export const defaultLanguage: UiLanguage = "EN";

export function normalizeLanguage(
  value: string | null | undefined,
  fallback: UiLanguage = defaultLanguage,
): UiLanguage {
  return value === "DE" || value === "EN" ? value : fallback;
}

export function languageFromCookieHeader(
  cookieHeader: string | null | undefined,
  fallback: UiLanguage = defaultLanguage,
): UiLanguage {
  const match = cookieHeader?.match(/(?:^|;\s*)unveiled_lang=(DE|EN)(?:;|$)/);
  return normalizeLanguage(match?.[1], fallback);
}

export function copyFor(language: string | null | undefined) {
  return appCopy[normalizeLanguage(language)];
}

